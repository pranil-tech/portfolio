
function showEducation() {
    document.getElementById('education-container').style.display = 'block';
    document.getElementById('internship-container').style.display = 'none';
}

function showInternships() {
    document.getElementById('education-container').style.display = 'none';
    document.getElementById('internship-container').style.display = 'block';
}

function showSkills() {
    document.getElementById('education-container').style.display = 'none';
    document.getElementById('internship-container').style.display = 'none';
}
